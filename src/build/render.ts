import path from "node:path";

import { minimatch } from "minimatch";

import type {
  ResolvedRegistryEntry,
  ScreenDefinition,
} from "../authoring/types.js";
import { toPosixPath } from "../config/paths.js";
import { isPublicStaticFile } from "../config/public_files.js";
import type { ResolvedConfig } from "../config/types.js";
import { MokabookError, errorMessage } from "../errors.js";
import type { Renderer } from "../renderer/types.js";
import { serializeReviewSentinels } from "../renderer/sentinels.js";
import { fragmentRoute } from "../registry/manifest.js";
import { rewriteMockLinks } from "./mock_links.js";

/** Header proving Mokabook owns a generated HTML file. */
export const GENERATED_HEADER = "<!-- Generated by mokabook";

/** Render every screen viewport to owned, linked static documents. */
export function renderFragments(
  entries: readonly ResolvedRegistryEntry[],
  renderer: Renderer,
  config: ResolvedConfig,
): Map<string, string> {
  const byId = new Map(entries.map((entry) => [entry.id, entry]));
  const outputs = new Map<string, string>();
  for (const entry of entries) {
    if (entry.kind !== "screen") continue;
    for (const viewport of ["mobile", "desktop"] as const) {
      const route = fragmentRoute(entry.route, viewport);
      const stylesheets = stylesheetsFor(route, config);
      let rendered: string;
      try {
        rendered = renderer({
          entry: entry as ScreenDefinition,
          node: entry[viewport],
          stylesheets,
          viewport,
        });
      } catch (error) {
        throw new MokabookError(
          "build-invalid",
          `renderer failed for ${entry.id} (${viewport}): ${errorMessage(error)}`,
          { cause: error },
        );
      }
      if (typeof rendered !== "string" || !/<html[\s>]/i.test(rendered)) {
        throw new MokabookError(
          "build-invalid",
          `renderer must return a complete HTML document for ${entry.id} (${viewport})`,
        );
      }
      const linked = rewriteMockLinks(
        serializeReviewSentinels(rendered),
        route,
        viewport,
        byId,
      );
      addOutput(
        outputs,
        route,
        `${generatedHeader(entry.sourceRelativePath)}${linked}`,
      );
    }
  }
  return outputs;
}

/** Add one output and fail on a route collision. */
export function addOutput(
  outputs: Map<string, string>,
  route: string,
  content: string,
): void {
  if (outputs.has(route)) {
    throw new MokabookError(
      "build-invalid",
      `generated route collision: ${route}`,
    );
  }
  outputs.set(route, content.endsWith("\n") ? content : `${content}\n`);
}

/** Create the generic ownership header for a generated document. */
export function generatedHeader(sourceRelativePath: string): string {
  return `${GENERATED_HEADER} from ${sourceRelativePath}. Do not edit. -->\n`;
}

function stylesheetsFor(route: string, config: ResolvedConfig): string[] {
  const rule = config.stylesheets.find((candidate) =>
    minimatch(route, candidate.match),
  );
  if (!rule) return [];
  return rule.stylesheets.map((stylesheet) => {
    if (/^https?:\/\//.test(stylesheet)) return stylesheet;
    const absolute = path.resolve(config.mockupsDir, stylesheet);
    if (!isPublicStaticFile(absolute, config)) {
      throw new MokabookError(
        "build-invalid",
        `stylesheet does not exist: ${stylesheet}`,
      );
    }
    const relative = path.posix.relative(path.posix.dirname(route), stylesheet);
    return relative.startsWith(".") ? relative : `./${relative}`;
  });
}

/** Normalize an absolute source path for deterministic diagnostics. */
export function sourceLabel(
  config: ResolvedConfig,
  sourcePath: string,
): string {
  return toPosixPath(path.relative(config.repoRoot, sourcePath));
}
